/**
 * Собирает assets/visuals/raster-icons.js из:
 * - PNG в assets/visuals/raster/skills/{id}.png (+ invoke_seal.png)
 * - оверрайдов assets/visuals/raster-icons.manual.js
 *
 * Run: npm run icons:raster-map
 */
import { existsSync, readdirSync, writeFileSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";
import { SKILLS_CONFIG } from "../skills.config.js";
import { VISUAL_RASTER_MANUAL } from "../assets/visuals/raster-icons.manual.js";
import {
  SKILL_ICON_ATLAS_REGIONS,
  SKILL_ICON_ATLAS_URL as ATLAS_PUBLIC_URL,
} from "../assets/visuals/skill-icons-atlas.gen.js";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const skillsDir = join(root, "assets", "visuals", "raster", "skills");
const outJs = join(root, "assets", "visuals", "raster-icons.js");
const outManifest = join(root, "assets", "visuals", "raster-icons.manifest.json");

const urlBase = "/assets/visuals/raster/skills";

/** @type {Map<string, { visualId: string, name: string, sequence: string }>} */
const bySkillId = new Map();

for (const row of SKILLS_CONFIG.singleRuneCombos) {
  bySkillId.set(row.id, { visualId: row.visualId, name: row.name, sequence: row.sequence });
}
for (const row of SKILLS_CONFIG.dualRuneCombos) {
  bySkillId.set(row.id, { visualId: row.visualId, name: row.name, sequence: row.sequence });
}
for (const row of SKILLS_CONFIG.invokeCombos) {
  bySkillId.set(row.id, { visualId: row.visualId, name: row.name, sequence: row.sequence });
}

const INVOKE_SEAL_VISUAL = "game_invoke_seal_circle";

/** @type {Record<string, string>} */
const urls = { ...VISUAL_RASTER_MANUAL };

if (existsSync(skillsDir)) {
  for (const name of readdirSync(skillsDir)) {
    if (!name.endsWith(".png")) continue;
    const base = name.slice(0, -4);
    let visualId;
    if (base === "invoke_seal") {
      visualId = INVOKE_SEAL_VISUAL;
    } else {
      const meta = bySkillId.get(base);
      if (!meta) {
        console.warn(`[raster-map] skip unknown file: ${name} (no skill id "${base}")`);
        continue;
      }
      visualId = meta.visualId;
    }
    urls[visualId] = `${urlBase}/${name}`;
  }
}

for (const k of Object.keys(SKILL_ICON_ATLAS_REGIONS)) {
  delete urls[k];
}

const keys = Object.keys(urls).sort();
const lines = keys.map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(urls[k])},`);
const js = `/**
 * Auto-generated by scripts/build-raster-icons-map.mjs — не править вручную.
 * Источники: assets/visuals/raster/skills/*.png + raster-icons.manual.js
 * Пересборка: npm run icons:raster-map
 */
export const VISUAL_RASTER_URLS = {
${lines.join("\n")}
};
`;

writeFileSync(outJs, js, "utf8");

/** @type {{ generatedAt: string; icons: Array<{ visualId: string; skillId: string | null; name: string | null; sequence: string | null; rasterUrl: string | null; source: "file" | "manual" }> }} */
const manifest = {
  generatedAt: new Date().toISOString(),
  icons: [],
};

const visualToSkillId = new Map();
for (const [skillId, meta] of bySkillId) {
  visualToSkillId.set(meta.visualId, skillId);
}

const fromFile = new Set();
if (existsSync(skillsDir)) {
  for (const name of readdirSync(skillsDir)) {
    if (!name.endsWith(".png")) continue;
    const base = name.slice(0, -4);
    const visualId = base === "invoke_seal" ? INVOKE_SEAL_VISUAL : bySkillId.get(base)?.visualId;
    if (visualId) fromFile.add(visualId);
  }
}

const fromAtlas = new Set(Object.keys(SKILL_ICON_ATLAS_REGIONS));

function pushEntry(visualId, skillId, name, sequence, rasterUrl, source) {
  manifest.icons.push({
    visualId,
    skillId,
    name,
    sequence,
    rasterUrl,
    source,
  });
}

function manifestSourceAndUrl(visualId) {
  if (fromAtlas.has(visualId)) {
    return { source: "atlas", url: ATLAS_PUBLIC_URL };
  }
  const u = urls[visualId] ?? null;
  if (fromFile.has(visualId)) return { source: "file", url: u };
  if (u) return { source: "manual", url: u };
  return { source: "svg", url: null };
}

for (const [skillId, meta] of bySkillId) {
  const { source, url } = manifestSourceAndUrl(meta.visualId);
  pushEntry(meta.visualId, skillId, meta.name, meta.sequence, url, source);
}

{
  const { source, url } = manifestSourceAndUrl(INVOKE_SEAL_VISUAL);
  pushEntry(INVOKE_SEAL_VISUAL, "invoke_seal", "Invoke seal (UI)", "—", url, source);
}

for (const row of [
  { visualId: "game_combo_rune_frost", name: "Combo rune glyph (Q / frost)", sequence: "Q" },
  { visualId: "game_combo_rune_lightning", name: "Combo rune glyph (W / lightning)", sequence: "W" },
  { visualId: "game_combo_rune_fire", name: "Combo rune glyph (E / fire)", sequence: "E" },
]) {
  const { source, url } = manifestSourceAndUrl(row.visualId);
  pushEntry(row.visualId, null, row.name, row.sequence, url, source);
}

writeFileSync(outManifest, JSON.stringify(manifest, null, 2), "utf8");

console.log("OK", outJs, keys.length, "raster entries");
console.log("OK", outManifest, manifest.icons.length, "rows");
