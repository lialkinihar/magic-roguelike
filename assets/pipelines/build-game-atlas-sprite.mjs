import { mkdir, readdir, writeFile } from "fs/promises";
import { existsSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";
import sharp from "sharp";
import { ATLAS_TILE_CELL, ATLAS_TILE_ORDER } from "./atlas-tiles.config.js";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..", "..");
const importDir = join(root, "assets", "pipeline-work", "import");
const refineDir = join(root, "assets", "pipeline-work", "refine");
const outDir = join(root, "assets", "visuals", "raster");
const outAtlasPath = join(outDir, "assets-atlas.png");
const outGenPath = join(root, "assets", "visuals", "assets-atlas.gen.js");

function parseArgs(argv) {
  return {
    useRefine: !argv.includes("--from-import"),
  };
}

async function readPngSet(dirPath) {
  const fileNames = (await readdir(dirPath)).filter((name) => name.endsWith(".png"));
  return new Set(fileNames.map((name) => name.replace(/\.png$/i, "")));
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  const inputDir = args.useRefine && existsSync(refineDir) ? refineDir : importDir;
  if (!existsSync(inputDir)) throw new Error(`[pack] missing input dir: ${inputDir}`);

  const available = await readPngSet(inputDir);
  for (const assetId of ATLAS_TILE_ORDER) {
    if (!available.has(assetId)) throw new Error(`[pack] missing PNG for assetId: ${assetId}`);
  }

  const cell = ATLAS_TILE_CELL;
  const n = ATLAS_TILE_ORDER.length;
  const cols = Math.max(1, Math.ceil(Math.sqrt(n)));
  const rows = Math.ceil(n / cols);
  const atlasWidth = cols * cell;
  const atlasHeight = rows * cell;

  const composites = [];
  /** @type {Record<string, { x: number, y: number, w: number, h: number }>} */
  const regions = {};

  for (let i = 0; i < ATLAS_TILE_ORDER.length; i++) {
    const assetId = ATLAS_TILE_ORDER[i];
    const col = i % cols;
    const row = Math.floor(i / cols);
    const resized = await sharp(join(inputDir, `${assetId}.png`))
      .ensureAlpha()
      .resize(cell, cell, {
        fit: "contain",
        position: "centre",
        background: { r: 0, g: 0, b: 0, alpha: 0 },
        kernel: sharp.kernel.lanczos3,
      })
      .png()
      .toBuffer();
    composites.push({ input: resized, left: col * cell, top: row * cell });
    regions[assetId] = { x: col * cell, y: row * cell, w: cell, h: cell };
  }

  await mkdir(outDir, { recursive: true });
  await sharp({
    create: {
      width: atlasWidth,
      height: atlasHeight,
      channels: 4,
      background: { r: 0, g: 0, b: 0, alpha: 0 },
    },
  })
    .composite(composites)
    .png({ compressionLevel: 9 })
    .toFile(outAtlasPath);

  const regionLines = ATLAS_TILE_ORDER.map((assetId) => {
    const r = regions[assetId];
    return `  ${JSON.stringify(assetId)}: { x: ${r.x}, y: ${r.y}, w: ${r.w}, h: ${r.h} },`;
  }).join("\n");

  const gen = `/**
 * Auto-generated by assets/pipelines/build-game-atlas-sprite.mjs
 */
export const ASSETS_ATLAS_URL = "/assets/visuals/raster/assets-atlas.png";
export const ASSETS_ATLAS_WIDTH = ${atlasWidth};
export const ASSETS_ATLAS_HEIGHT = ${atlasHeight};
export const ASSETS_ATLAS_CELL = ${cell};
/** @type {Record<string, { x: number; y: number; w: number; h: number }>} */
export const ASSETS_ATLAS_REGIONS = {
${regionLines}
};
`;
  await writeFile(outGenPath, gen, "utf8");
  console.log(`[pack] wrote ${outAtlasPath}`);
  console.log(`[pack] wrote ${outGenPath}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
