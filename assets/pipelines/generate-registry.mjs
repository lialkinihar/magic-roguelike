import { writeFile } from "fs/promises";
import { dirname, join } from "path";
import { fileURLToPath } from "url";
import { IMPORT_MANIFEST } from "../import-manifest.js";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..", "..");
const outPath = join(root, "assets", "visuals", "registry.js");

function familyForId(assetId) {
  if (assetId.startsWith("app_")) return "app";
  if (assetId.startsWith("game_skill_")) return "skill";
  if (assetId.startsWith("game_combo_rune_")) return "combo_rune";
  if (assetId.startsWith("game_shop_")) return "shop";
  if (assetId.startsWith("game_buff_")) return "buff";
  if (assetId.startsWith("game_artifact_")) return "artifact";
  return "game";
}

function buildRegistry() {
  /** @type {Record<string, { kind: "icon", family: string, source: "asset_atlas" | "svg_icons", path?: string }>} */
  const out = {};

  for (const row of IMPORT_MANIFEST) {
    if (!row?.assetId || !row?.source) continue;
    const id = row.assetId;
    if (row.source === "svg_icons") {
      out[id] = { kind: "icon", family: familyForId(id), source: "svg_icons", path: row.path };
      continue;
    }
    if (row.source === "asset_atlas") {
      out[id] = { kind: "icon", family: familyForId(id), source: "asset_atlas" };
    }
  }
  return out;
}

function serialize(registry) {
  const body = Object.keys(registry)
    .sort()
    .map((id) => `  ${JSON.stringify(id)}: ${JSON.stringify(registry[id])},`)
    .join("\n");
  return `/**
 * Auto-generated by assets/pipelines/generate-registry.mjs
 */
export const VISUAL_ASSETS = {
${body}
};
`;
}

const registry = buildRegistry();
await writeFile(outPath, serialize(registry), "utf8");
console.log(`[registry] wrote ${outPath} (${Object.keys(registry).length} ids)`);
